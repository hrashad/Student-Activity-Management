<center><h1 style="color: blueviolet;">The Build Club</h1>
<p style="font-size:25px">The Build Club – an initiative of the IITM Incubation Cell, driven by IIMRP, in association with MAMCET (PI) is now officially launched.</p>


<p style="font-family:sans-serif;">Aiming to inculcate and celebrate the culture of building things that have real-life functionalities, The Build Club, is a student-only run club enabling students to independently build their own products and in the long run, feel inspired to pursue entrepreneurship!</p>



<img src="build.jpeg" height="50%" width="75%"></br></br>
<p><b>The two-day event saw students participating from one among five different colleges, M.A.M. College of Engineering and Technology Maxelerator Foundation!</b>
<br><br>
Several exciting projects were showcased and demonstrated to the students which included building an autonomous robot that can navigate a maze (micro mouse), Building and porting an IDE software on Embedded Processor Board, Building IOT Systems – Smart water monitoring using water flow meter; Smart Energy Meter; Vehicle detection using RFID and building a mobile app to enable payment gateway using Flutter.
<br><br>
Several experts across the sectors of Network solutions, Financial inclusion, Innovation in the Agri-value chain, and Assistive Technologies also shared their insights with the students.</p>
